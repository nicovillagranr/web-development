// 2. EVENTOS DEL MOUSE
// ==============================

// Eventos principales
// - "click"       -> ocurre con un clic
// - "dblclick"    -> ocurre con un doble clic
// - "mouseover"   -> ocurre cuando el puntero entra en un elemento o en uno de sus hijos
// - "mouseout"    -> ocurre cuando el puntero sale de un elemento o de sus hijos

// Otros eventos útiles
// - "contextmenu" -> clic derecho para abrir menú contextual
// - "mouseenter"  -> ocurre cuando el puntero entra en un elemento (sin contar hijos)
// - "mouseleave"  -> ocurre cuando el puntero sale de un elemento (sin contar hijos)
// - "mouseup"     -> cuando se suelta un botón del mouse sobre un elemento
// - "mousemove"   -> ocurre mientras el puntero se mueve sobre un elemento

// ==============================
// 3. EVENTOS DEL TECLADO
// ==============================
// - "keydown"   -> ocurre cuando una tecla se PRESIONA (antes de soltarla)
// - "keypress"  -> ocurre cuando una tecla produce un carácter (está deprecado, no se recomienda usarlo)
// - "keyup"     -> ocurre cuando se SUELTA una tecla

// ==============================
// 4. EVENTOS DE LA INTERFAZ
// ==============================
// - "error"       -> ocurre cuando sucede un error al cargar un archivo multimedia
// - "load"        -> ocurre cuando un objeto se ha cargado
// - "beforeunload"-> ocurre antes de que el documento esté a punto de descargarse
// - "resize"      -> ocurre cuando se cambia el tamaño de la ventana
// - "scroll"      -> ocurre cuando se desplaza la barra de scroll
// - "select"      -> ocurre cuando el usuario selecciona texto en <input> o <textarea>

// ==============================
// 5. TIMERS (TEMPORIZADORES)
// ==============================
// - setTimeout()    -> ejecuta una función después de un tiempo
// - setInterval()   -> ejecuta una función repetidamente cada cierto intervalo
// - clearTimeout()  -> cancela un setTimeout
// - clearInterval() -> cancela un setInterval
